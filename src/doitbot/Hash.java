
package doitbot;



/**
 * Clase para crear el objeto hash obtenido del XML
 * @author Nicolas Scordamaglia
 */
class Hash {
    
    String value;

    

    public Hash(){
    
        this.value = null;
    
    }
    
    
    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
    
    
       
}
